using 変成将棋.Models;

namespace 変成将棋.Engine.Tests;

/// <summary>
/// Is王手放置（王手放置フィルタ）の正確性テスト。
/// Get合法手の結果が「手を指した後に自玉が相手の攻撃下にある手を含まない」ことを検証する。
///
/// 検証方法：
///   合法手リストに含まれる各手を Apply し、
///   C利き管理.Compute全利き による正確な利きビットで
///   自玉が攻撃されていないことをアサートする。
/// </summary>
public class Is王手放置Tests
{
    private static S升座標 Sq(int 列, int 段) => new((byte)列, (byte)段);

    // 合法手全てについて「適用後に自玉が相手利きに入っていない」ことを検証する汎用ヘルパー
    private static void Assert合法手に王手放置なし(C盤面 盤面)
    {
        var 手番 = 盤面.手番;
        Span<S手> バッファ = stackalloc S手[C合法手生成器.最大手数];
        int 手数 = C合法手生成器.Get合法手(盤面, バッファ);

        for (int i = 0; i < 手数; i++)
        {
            var 手 = バッファ[i];
            var 取消 = 盤面.Apply(手);
            // 正確な利きで自玉が相手に攻撃されていないことを確認
            var (先手利き, 後手利き) = C利き管理.Compute全利き(盤面);
            var 相手利き = 手番 == E手番.先手 ? 後手利き : 先手利き;
            var 玉 = 盤面.Find玉(手番);
            Assert.True(玉.Is有効, $"手 {i}: 自玉が消えた");
            Assert.False(相手利き.Contains(玉),
                $"手 {i}: 王手放置が合法手に含まれている（{手}）");
            盤面.Undo(手, 取消);
        }
    }

    // 擬似合法手のうち「王手放置」に相当する手が合法手リストに含まれないことを確認
    private static void Assert王手放置が除外されている(C盤面 盤面)
    {
        var 手番 = 盤面.手番;
        Span<S手> 全バッファ = stackalloc S手[C合法手生成器.最大手数];
        Span<S手> 合法バッファ = stackalloc S手[C合法手生成器.最大手数];

        int 全手数 = C合法手生成器.Generate擬似合法手(盤面, 全バッファ);
        int 合法手数 = C合法手生成器.Get合法手(盤面, 合法バッファ);

        // 合法手に入っていないがある擬似合法手が「王手放置」であることを確認
        int 除外数 = 0;
        for (int i = 0; i < 全手数; i++)
        {
            var 手 = 全バッファ[i];
            bool 合法にある = false;
            for (int j = 0; j < 合法手数; j++)
                if (合法バッファ[j].Equals(手)) { 合法にある = true; break; }

            if (!合法にある)
            {
                // 除外された手 → 王手放置のはず
                var 取消 = 盤面.Apply(手);
                var (先手利き, 後手利き) = C利き管理.Compute全利き(盤面);
                var 相手利き = 手番 == E手番.先手 ? 後手利き : 先手利き;
                var 玉 = 盤面.Find玉(手番);
                bool 放置 = !玉.Is有効 || 相手利き.Contains(玉);
                Assert.True(放置, $"合法手から除外されたが王手放置でない: {手}");
                盤面.Undo(手, 取消);
                除外数++;
            }
        }
        return; // 除外数はケースによって異なるため件数はここでは検証しない
    }

    // ─── テストケース ────────────────────────────────────────────────

    [Fact]
    public void 直接王手_飛車縦_合法手は逃げ手のみ()
    {
        // 先手玉(5,9)を後手飛車(5,1)が縦に王手している
        // 列5に駒がいないため後手飛車の利きが通る
        // SFEN: 先手手番、先手玉at(5,9)、後手飛車at(5,1)、後手玉at(1,1)
        // 盤面文字列: 段1=k3r4, 段9=4K4
        var 盤面 = new C盤面("k3r4/9/9/9/9/9/9/9/4K4 b -");

        Assert合法手に王手放置なし(盤面);

        // 王手されているので合法手は限られるはず（0手ではない）
        Span<S手> バッファ = stackalloc S手[C合法手生成器.最大手数];
        int 手数 = C合法手生成器.Get合法手(盤面, バッファ);
        Assert.True(手数 > 0, "王手から逃げる手が0手（詰みでないはずなのに）");
    }

    [Fact]
    public void 空き王手放置_銀が守備をはずれると王手になる()
    {
        // 先手玉(5,9)、先手銀将(5,5)、後手飛車(5,1)、後手玉(1,1)
        // 先手銀(5,5)が斜めに移動すると先手玉が後手飛車にさらされる（空き王手放置）
        // SFEN: k3r4/9/9/9/4S4/9/9/9/4K4 b -
        // 注: 後手飛車は段1列5にあり、先手銀が段5列5でブロック
        var 盤面 = new C盤面("k3r4/9/9/9/4S4/9/9/9/4K4 b -");

        Assert合法手に王手放置なし(盤面);
        Assert王手放置が除外されている(盤面);
    }

    [Fact]
    public void 空き王手放置_銀がはずれる手は合法手から除外される()
    {
        // 上と同じ局面で、銀の横移動（王手放置）が除外されて
        // 銀の前進（守備継続）が合法手に含まれることを確認
        var 盤面 = new C盤面("k3r4/9/9/9/4S4/9/9/9/4K4 b -");

        Span<S手> バッファ = stackalloc S手[C合法手生成器.最大手数];
        int 手数 = C合法手生成器.Get合法手(盤面, バッファ);

        // 銀が段5列5から段4列5（直前）への移動は合法（ブロック継続）
        bool 直前移動が合法 = false;
        bool 斜め移動が除外 = true; // 斜めへの移動（列を変える）は除外されているはず

        for (int i = 0; i < 手数; i++)
        {
            var 手 = バッファ[i];
            if (!手.Is打ち && 手.Get移動元.Equals(Sq(5, 5)) && 手.Get移動先.Equals(Sq(5, 4)))
                直前移動が合法 = true;
            // 銀が列5以外に移動する手があったら除外されていない（バグ）
            if (!手.Is打ち && 手.Get移動元.Equals(Sq(5, 5)) && 手.Get移動先.列 != 5)
                斜め移動が除外 = false;
        }

        Assert.True(直前移動が合法, "銀の直前移動（守備継続）が合法手に含まれていない");
        Assert.True(斜め移動が除外, "銀の斜め移動（王手放置）が合法手に残っている（バグ）");
    }

    [Fact]
    public void 直接王手_角行斜め_合法手は全て正しい()
    {
        // 先手玉(9,9)を後手角行(1,1)が斜めに王手
        // 間に駒なし
        // SFEN: b8/9/9/9/9/9/9/9/8K b -
        // 注: bは後手角行 lowercase, K は先手玉将
        var 盤面 = new C盤面("b8/9/9/9/9/9/9/9/8K b -");

        Assert合法手に王手放置なし(盤面);

        Span<S手> バッファ = stackalloc S手[C合法手生成器.最大手数];
        int 手数 = C合法手生成器.Get合法手(盤面, バッファ);
        Assert.True(手数 > 0);
    }

    [Fact]
    public void 王手でない局面_全擬似合法手が合法()
    {
        // 先手玉(5,9)、後手玉(5,1)、相互に王手なし（列5のみで間に距離）
        // SFEN: 4k4/9/9/9/9/9/9/9/4K4 b -
        var 盤面 = new C盤面("4k4/9/9/9/9/9/9/9/4K4 b -");

        // 王手がかかっていないので擬似合法手=合法手のはず
        Span<S手> 全バッファ = stackalloc S手[C合法手生成器.最大手数];
        Span<S手> 合法バッファ = stackalloc S手[C合法手生成器.最大手数];
        int 全手数 = C合法手生成器.Generate擬似合法手(盤面, 全バッファ);
        int 合法手数 = C合法手生成器.Get合法手(盤面, 合法バッファ);

        // 玉が隣接していない場合、全擬似合法手 ≥ 合法手数
        Assert.True(合法手数 <= 全手数);
        Assert合法手に王手放置なし(盤面);
    }

    [Fact]
    public void 香車王手_先手玉を後手香車が縦に攻撃()
    {
        // 先手玉(5,9)、後手香車(5,2)（後手香車は段増加方向に進む）
        // 後手香車(5,2)は段3,4,...,9に利く → 先手玉(5,9)を攻撃
        // ただし間に駒なし
        // SFEN: 4k4/4l4/9/9/9/9/9/9/4K4 b -
        var 盤面 = new C盤面("4k4/4l4/9/9/9/9/9/9/4K4 b -");

        Assert合法手に王手放置なし(盤面);
        Assert王手放置が除外されている(盤面);

        // 先手玉は王手されているので逃げる手が必要
        Span<S手> バッファ = stackalloc S手[C合法手生成器.最大手数];
        int 手数 = C合法手生成器.Get合法手(盤面, バッファ);
        Assert.True(手数 > 0);
    }

    [Fact]
    public void 駒ビット整合性_ApplyUndoで駒ビットが復元される()
    {
        // Apply → Undo 後に各駒種の位置ビットが元に戻ることを確認
        var 盤面 = new C盤面("k3r4/9/9/9/4S4/9/9/9/4K4 b -");

        // 適用前のスナップショット
        var 銀前 = 盤面.Get駒ビット(E手番.先手, E駒種.銀将);
        var 玉前 = 盤面.Get駒ビット(E手番.先手, E駒種.玉将);

        Span<S手> バッファ = stackalloc S手[C合法手生成器.最大手数];
        int 手数 = C合法手生成器.Generate擬似合法手(盤面, バッファ);
        Assert.True(手数 > 0);

        // 最初の手を Apply → Undo
        var 手 = バッファ[0];
        var 取消 = 盤面.Apply(手);
        盤面.Undo(手, 取消);

        // Undo後は元に戻っているはず
        var 銀後 = 盤面.Get駒ビット(E手番.先手, E駒種.銀将);
        var 玉後 = 盤面.Get駒ビット(E手番.先手, E駒種.玉将);

        Assert.True(銀前.Xor(銀後).IsEmpty, "Undo後に先手銀将の駒ビットが変わっている");
        Assert.True(玉前.Xor(玉後).IsEmpty, "Undo後に先手玉将の駒ビットが変わっている");
    }

    [Fact]
    public void 駒ビット整合性_盤面から駒ビットが正しく生成される()
    {
        // SFEN: 先手銀が(5,5)、先手玉が(5,9)
        var 盤面 = new C盤面("k3r4/9/9/9/4S4/9/9/9/4K4 b -");

        var 銀ビット = 盤面.Get駒ビット(E手番.先手, E駒種.銀将);
        var 玉ビット = 盤面.Get駒ビット(E手番.先手, E駒種.玉将);
        var 飛ビット = 盤面.Get駒ビット(E手番.後手, E駒種.飛車);

        Assert.True(銀ビット.Contains(Sq(5, 5)), "先手銀将(5,5)が駒ビットにない");
        Assert.True(玉ビット.Contains(Sq(5, 9)), "先手玉将(5,9)が駒ビットにない");
        Assert.True(飛ビット.Contains(Sq(5, 1)), "後手飛車(5,1)が駒ビットにない");

        // 他の升は含まない
        Assert.False(銀ビット.Contains(Sq(5, 4)), "先手銀将が(5,4)にある（誤）");
        Assert.False(玉ビット.Contains(Sq(5, 5)), "先手玉将が(5,5)にある（誤）");
    }

    [Fact]
    public void Is王手放置_各合法手後に自玉が安全である()
    {
        // 初期局面から数手進め、各局面でGet合法手の結果に王手放置が含まれないことを確認する。
        // （利きビット差分更新はXOR-ANDバグで不正確だがIs王手放置はそれに依存しないため
        //   このテストはマジックビットボードによるIs王手放置の正確性を検証する）
        var 盤面 = new C盤面();
        Span<S手> バッファ = stackalloc S手[C合法手生成器.最大手数];

        for (int turn = 0; turn < 6; turn++)
        {
            Assert合法手に王手放置なし(盤面);

            int 手数 = C合法手生成器.Get合法手(盤面, バッファ);
            Assert.True(手数 > 0, $"手数が0（手番{turn}）");

            // 最初の手を指して次のターンへ
            盤面.Apply(バッファ[0]);
        }
    }

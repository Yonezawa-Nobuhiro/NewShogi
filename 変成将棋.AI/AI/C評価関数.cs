using 変成将棋.AI.αβAI;
using 変成将棋.Models;

namespace 変成将棋.AI;

// 評価は「現手番視点」：正 = 現手番が有利、負 = 現手番が不利。
// 1. 駒得               : 盤上駒 + 持ち駒の合計価値差
// 2. 王危険度           : 玉周囲（チェビシェフ距離2）の相手駒数
// 3. 位置ボーナス       : 各駒の前進度・中央列ボーナス、玉は端列×自陣の2次元ボーナス
// 4. 攻め込みボーナス   : 敵玉チェビシェフ距離3以内にいる自駒数（距離が近いほど高得点）
// 5. 打ち込みポテンシャル: 持ち駒を敵陣に打てる升数
public static class C評価関数
{
    // ── 前進ボーナステーブル [前進度 0=自陣 .. 8=相手陣最深] ──────────
    // 先手: 前進度 = 9 - 段  （段9=0, 段1=8）
    // 後手: 前進度 = 段 - 1  （段1=0, 段9=8）

    private static readonly int[] 歩前進 = [  0,  0,  0,  5, 10, 20, 35, 50, 70];
    private static readonly int[] 香前進 = [  0,  0,  3,  6, 10, 15, 20, 25, 30];
    private static readonly int[] 桂前進 = [  0,  0,  5, 10, 15, 15, 10,  5,  0];
    private static readonly int[] 銀前進 = [  0,  5, 10, 15, 10,  5,  0,  0,  0];
    private static readonly int[] 金前進 = [  0,  3,  8, 10,  5,  0,  0,  0,  0];
    private static readonly int[] 飛前進 = [  0,  5, 10, 15, 10,  5,  0,  0,  0];

    // 玉の段ボーナス：自陣にいるほど高い
    private static readonly int[] 玉段  = [ 30, 20,  5,  0,-15,-30,-45,-60,-70];
                                           // 前進度0=自陣最深(+30) .. 8=敵陣最深(-70)

    // 玉の列ボーナス：端（1or9）ほど高い（先後共通、列は絶対値）
    private static readonly int[] 玉列  = [ 60, 35, 10,  0,-20,  0, 10, 35, 60];
                                           // 列1,  列2, 列3, 列4, 列5(中央), 列6, 列7, 列8, 列9

    // ── 公開評価 ────────────────────────────────────────────────────

    public static int Evaluate(C盤面 盤面, αβパラメータ p, int[] 駒価値)
    {
        int score = 0;
        var 手番 = 盤面.手番;
        var 相手 = 手番 == E手番.先手 ? E手番.後手 : E手番.先手;

        // 1 & 3: 盤上駒の価値 + 位置ボーナス
        for (int 段 = 1; 段 <= 9; 段++)
        for (int 列 = 1; 列 <= 9; 列++)
        {
            var 駒 = 盤面.Get駒(列, 段);
            if (!駒.Is有効) continue;

            int 前進度 = 駒.手番 == E手番.先手 ? (9 - 段) : (段 - 1);
            int 価値   = 駒価値[(int)駒.種類];
            int 位置   = 位置ボーナス(駒.種類, 前進度, 列) * p.位置ボーナス重み / 100;

            if (駒.手番 == 手番) score += 価値 + 位置;
            else                 score -= 価値 + 位置;
        }

        // 1: 持ち駒ボーナス
        score += 持ち駒スコア(手番 == E手番.先手 ? 盤面.先手持ち駒 : 盤面.後手持ち駒,
                              駒価値) * p.持ち駒ボーナス重み / 100;
        score -= 持ち駒スコア(手番 == E手番.先手 ? 盤面.後手持ち駒 : 盤面.先手持ち駒,
                              駒価値) * p.持ち駒ボーナス重み / 100;

        // 2: 王危険度
        var 自玉 = 盤面.Find玉(手番);
        var 敵玉 = 盤面.Find玉(相手);
        if (自玉.Is有効) score -= 王危険度(盤面, 自玉, 相手) * p.王危険度重み;
        if (敵玉.Is有効) score += 王危険度(盤面, 敵玉, 手番) * p.王危険度重み;

        // 4: 攻め込みボーナス（敵玉距離3以内の自駒）
        if (敵玉.Is有効)
            score += 攻め込みスコア(盤面, 敵玉, 手番) * p.攻め込み重み;
        if (自玉.Is有効)
            score -= 攻め込みスコア(盤面, 自玉, 相手) * p.攻め込み重み;

        // 5: 打ち込みポテンシャル（持ち駒を敵玉周辺に打てる升数）
        var 自持ち駒 = 手番 == E手番.先手 ? 盤面.先手持ち駒 : 盤面.後手持ち駒;
        var 敵持ち駒 = 手番 == E手番.先手 ? 盤面.後手持ち駒 : 盤面.先手持ち駒;
        if (敵玉.Is有効)
            score += 打ち込みポテンシャル(手番, 敵玉, 自持ち駒) * p.打ち込みポテンシャル重み;
        if (自玉.Is有効)
            score -= 打ち込みポテンシャル(相手, 自玉, 敵持ち駒) * p.打ち込みポテンシャル重み;

        return score;
    }

    // ── 内部ヘルパー ────────────────────────────────────────────────

    private static int 位置ボーナス(E駒種 種類, int 前進度, int 列)
    {
        int 前進 = 種類 switch
        {
            E駒種.歩兵                  => 歩前進[前進度],
            E駒種.香車                  => 香前進[前進度],
            E駒種.桂馬                  => 桂前進[前進度],
            E駒種.銀将 or E駒種.麒麟   => 銀前進[前進度],
            E駒種.金将 or E駒種.と金
                or E駒種.竪行
                or E駒種.騎兵
                or E駒種.鳳凰          => 金前進[前進度],
            E駒種.飛車 or E駒種.龍王   => 飛前進[前進度],
            // 玉将・獅王：段ボーナス + 列ボーナス（端ほど高い）
            E駒種.玉将 or E駒種.獅王   => 玉段[前進度] + 玉列[列 - 1],
            _                           => 0,
        };

        // 走り駒は中央列が使いやすい
        int 中央 = 種類 switch
        {
            E駒種.角行 or E駒種.龍馬
                or E駒種.飛車
                or E駒種.龍王
                or E駒種.竪行 => Math.Max(0, 15 - (Math.Abs(列 - 5) * 3)),
            _                  => 0,
        };

        return 前進 + 中央;
    }

    private static int 持ち駒スコア(int[] 持ち駒, int[] 駒価値)
    {
        int total = 0;
        for (int t = 1; t < 持ち駒.Length; t++)
            if (持ち駒[t] > 0) total += 駒価値[t] * 持ち駒[t];
        return total;
    }

    // 敵玉チェビシェフ距離3以内にいる自駒数（距離が近いほど高得点）
    // 距離1=3点, 距離2=2点, 距離3=1点  玉自体は除く
    private static int 攻め込みスコア(C盤面 盤面, S升座標 敵玉位置, E手番 攻撃側)
    {
        int score = 0;
        for (int 段 = 1; 段 <= 9; 段++)
        for (int 列 = 1; 列 <= 9; 列++)
        {
            var 駒 = 盤面.Get駒(列, 段);
            if (!駒.Is有効 || 駒.手番 != 攻撃側) continue;
            if (駒.種類 is E駒種.玉将 or E駒種.獅王) continue;
            int 距離 = Math.Max(Math.Abs(列 - 敵玉位置.列), Math.Abs(段 - 敵玉位置.段));
            if (距離 <= 3) score += 4 - 距離;
        }
        return score;
    }

    // 持ち駒を敵陣（玉周辺3升以内）に打てる升数
    private static int 打ち込みポテンシャル(E手番 手番, S升座標 敵玉位置, int[] 持ち駒)
    {
        int 枚数合計 = 0;
        for (int t = 1; t < 持ち駒.Length; t++) 枚数合計 += 持ち駒[t];
        if (枚数合計 == 0) return 0;

        // 敵玉周辺3×3升（玉自体含む）のうち打てる升の数
        int 打てる升 = 0;
        for (int 段 = Math.Max(1, 敵玉位置.段 - 2); 段 <= Math.Min(9, 敵玉位置.段 + 2); 段++)
        for (int 列 = Math.Max(1, 敵玉位置.列 - 2); 列 <= Math.Min(9, 敵玉位置.列 + 2); 列++)
        {
            // 敵陣かつ行き所がある升に打てれば加点
            bool 敵陣 = 手番 == E手番.先手 ? 段 <= 3 : 段 >= 7;
            if (敵陣) 打てる升++;
        }
        return 枚数合計 * 打てる升;
    }

    // 玉の周囲チェビシェフ距離2以内にいる攻撃側の駒数
    private static int 王危険度(C盤面 盤面, S升座標 玉位置, E手番 攻撃側)
    {
        var 範囲 = C攻撃テーブル.獅王(玉位置);
        int 危険 = 0;
        for (int 段 = 1; 段 <= 9; 段++)
        for (int 列 = 1; 列 <= 9; 列++)
        {
            var 升 = new S升座標((byte)列, (byte)段);
            var _d = 盤面.Get駒(列, 段);
            if (範囲.Contains(升) && _d.Is有効 && _d.手番 == 攻撃側)
                危険++;
        }
        return 危険;
    }
}

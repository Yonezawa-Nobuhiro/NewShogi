namespace 変成将棋.Models;

public readonly struct C駒
{
    public E駒種 種類 { get; }
    public E手番 手番 { get; }

    public static readonly C駒 空 = new(E駒種.なし, default);
    public bool Is有効 => 種類 != E駒種.なし;

    public C駒(E駒種 種類, E手番 手番)
    {
        this.種類 = 種類;
        this.手番 = 手番;
    }

    // 駒種を成り後に変換する
    public static E駒種 Get成り後(E駒種 種類) => 種類 switch
    {
        E駒種.歩兵 => E駒種.と金,
        E駒種.香車 => E駒種.竪行,
        E駒種.桂馬 => E駒種.騎兵,
        E駒種.銀将 => E駒種.麒麟,
        E駒種.金将 => E駒種.鳳凰,
        E駒種.角行 => E駒種.龍馬,
        E駒種.飛車 => E駒種.龍王,
        E駒種.玉将 => E駒種.獅王,
        _ => 種類,
    };

    // 駒種を成り前に戻す（持ち駒管理や手の取り消しに使用）
    public static E駒種 Get成り前(E駒種 種類) => 種類 switch
    {
        E駒種.と金 => E駒種.歩兵,
        E駒種.竪行 => E駒種.香車,
        E駒種.騎兵 => E駒種.桂馬,
        E駒種.麒麟 => E駒種.銀将,
        E駒種.鳳凰 => E駒種.金将,
        E駒種.龍馬 => E駒種.角行,
        E駒種.龍王 => E駒種.飛車,
        E駒種.獅王 => E駒種.玉将,
        _ => 種類,
    };

    // 成ることができる駒種か（玉将の成りは特別ルールのため別途判定）
    public static bool Is成れる(E駒種 種類) => 種類 is
        E駒種.歩兵 or E駒種.香車 or E駒種.桂馬 or E駒種.銀将 or
        E駒種.金将 or E駒種.角行 or E駒種.飛車;

    public string Get表示文字() => 種類 switch
    {
        E駒種.歩兵 => "歩",
        E駒種.香車 => "香",
        E駒種.桂馬 => "桂",
        E駒種.銀将 => "銀",
        E駒種.金将 => "金",
        E駒種.角行 => "角",
        E駒種.飛車 => "飛",
        E駒種.玉将 => "玉",
        E駒種.と金 => "と",
        E駒種.竪行 => "竪",
        E駒種.騎兵 => "騎",
        E駒種.麒麟 => "麒",
        E駒種.鳳凰 => "鳳",
        E駒種.龍馬 => "馬",
        E駒種.龍王 => "龍",
        E駒種.獅王 => "獅",
        _ => "",
    };
}
